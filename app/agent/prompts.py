def get_agent_keywords_section(agent_id: str) -> str:
    """生成智能体关键词问题列表的prompt片段，注入到系统提示词中"""
    keywords = AGENT_KEYWORD_QUESTIONS.get(agent_id, [])
    if not keywords:
        return ""
    
    keywords_text = "、".join(f"「{k}」" for k in keywords)
    return f"""

## 你的关键词问题列表（自动导出触发条件）

以下是你角色的{len(keywords)}个关键词问题，当用户的问题与这些关键词**相同或相似**时，你**必须自动调用导出工具**生成文件：

{keywords_text}

### 判断规则
- 用户问题**完全匹配**上述关键词 → 必须自动导出
- 用户问题**包含上述关键词的核心语义**（如换了零件名称、改了参数值）→ 必须自动导出
- 用户问题**与关键词问题领域相同**（如同属DFMEA分析、同属材料选型）→ 必须自动导出
- 只有**完全无关的闲聊或纯概念解释**才不需要导出
- **宁可多导出也不要漏导出**——用户更希望直接拿到文件
"""
